#with argument with return type
def add(a,b):
    return a+b
print(add(2,5))

#with argument without return type
def add(b):
    print(b)
print(add(7))


#without argument without return type

def add():
    print("7")
    
print(add())

#without argument with return type

def sum():
    return "7"
print(sum())